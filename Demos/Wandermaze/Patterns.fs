module Patterns

open SDL

type Pattern = 
    {data:int list; width:int}

let makePattern (lines:string list, width:int) : Pattern =

    let lineToData (text:string) : int = 
        [0..(text.Length-1)]
        |> List.map (fun index -> text.Substring(index,1)="X")
        |> List.fold (fun (acc,flag) value -> if value then (acc+flag,flag * 2) else (acc, flag*2)) (0,1)
        |> fst

    let data =
        lines
        |> List.map lineToData

    {data=data; width = width}

let renderPattern (renderer:Render.Renderer) (scale:Geometry.Point) (background:Pixel.Color option) (foreground:Pixel.Color option) (offset:Geometry.Point) (pattern:Pattern) : unit = 
    [0..(pattern.data.Length-1)]
    |> List.iter
        (fun patternY -> 
            let line = pattern.data |> List.skip patternY |> List.head
            [0..(pattern.width-1)]
            |> List.fold
                (fun acc patternX ->
                    if (line &&& acc) <> 0 then
                        if foreground.IsSome then
                            Render.setDrawColor foreground.Value renderer |> ignore
                            Render.fillRectangle (Some {Geometry.Rectangle.X = (patternX + offset.X) * scale.X; Y = (patternY + offset.Y) * scale.Y; Width = scale.X; Height = scale.Y}) renderer |> ignore
                    else
                        if background.IsSome then
                            Render.setDrawColor background.Value renderer |> ignore
                            Render.fillRectangle (Some {Geometry.Rectangle.X = (patternX + offset.X) * scale.X; Y = (patternY + offset.Y) * scale.Y; Width = scale.X; Height = scale.Y}) renderer |> ignore
                    acc * 2) 1
            |> ignore)

let rec renderPatterns (renderer:Render.Renderer) (scale:Geometry.Point) (background:Pixel.Color option) (foreground:Pixel.Color option) (nextOffset:Geometry.Point -> Geometry.Point) (offset:Geometry.Point) (patterns:Pattern list) : unit = 
    match patterns with
    | [] -> ()
    | head :: tail ->
        renderPattern renderer scale background foreground offset head
        renderPatterns renderer scale background foreground nextOffset (offset |> nextOffset) tail


